def snowflake_side(length, levels):
	if levels == 0:
		forward(length)
		return
	length /= 2.5
	snowflake_side(length, levels - 1)
	left(60)
	snowflake_side(length, levels - 1)
	right(120)
	snowflake_side(length, levels - 1)
	left(60)
	snowflake_side(length, levels - 1)


def create_snowflake(sides, length):

	colors = ["red", "black" , "blue"]
	for i in range(sides):
		color(colors[i])
		snowflake_side(length, sides)
		right(360 / sides)


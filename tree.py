def tree(size, levels, angle):
	if levels == 0:
		color("red")
		dot()
		color("red")
		return

	forward(size)
	right(angle)
	tree(size*0.8, levels - 1, angle)
	left(angle * 2)
	tree(size * 0.7, levels - 1, angle)
	right(angle)
	tree(size * 0.7, levels - 1, angle)#extra final stems
	backward(size)
	tree(size, levels - 1, angle)#extra branches
